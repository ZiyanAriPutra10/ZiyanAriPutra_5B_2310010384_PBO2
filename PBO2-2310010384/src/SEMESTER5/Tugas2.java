 private void setupEvents() {
        KonversiButton.addActionListener(evt -> konversiSuhu());

        inputField.addKeyListener(new java.awt.event.KeyAdapter() {
            public void keyTyped(java.awt.event.KeyEvent evt) {
                char c = evt.getKeyChar();
                if (!((c >= '0' && c <= '9') || c == '.' || c == '-' ||
                        c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE)) {
                    evt.consume();
                    getToolkit().beep();
                }
                if (c == '.' && inputField.getText().contains(".")) {
                    evt.consume();
                    getToolkit().beep();
                }
    
                if (c == '-' && inputField.getText().length() > 0) {
                    evt.consume();
                    getToolkit().beep();
                }
            }
        });

        manualRadio.addItemListener(evt -> {
            if (autoRadio.isSelected()) {
                KonversiButton.setEnabled(false);
                konversiSuhu();
            } else {
                KonversiButton.setEnabled(true);
            }
        });

        autoRadio.addItemListener(evt -> {
            if (autoRadio.isSelected()) {
                konversiSuhu();
                KonversiButton.setEnabled(false);
            } else {
                KonversiButton.setEnabled(true);
            }
        });

        dariComboBox.addItemListener(evt -> {
            if (autoRadio.isSelected()) konversiSuhu();
        });

        keComboBox.addItemListener(evt -> {
            if (autoRadio.isSelected()) konversiSuhu();
        });

        inputField.getDocument().addDocumentListener(new DocumentListener() {
            public void insertUpdate(DocumentEvent evt) { if (autoRadio.isSelected()) konversiSuhu(); }
            public void removeUpdate(DocumentEvent evt) { if (autoRadio.isSelected()) konversiSuhu(); }
            public void changedUpdate(DocumentEvent evt) { if (autoRadio.isSelected()) konversiSuhu(); }
        });
    }

    private void konversiSuhu() {
        try {
            String inputText = inputField.getText().trim();
            if (inputText.isEmpty() || inputText.equals("-") || inputText.equals(".")) {
                hasilField.setText("");
                return;
            }

            double suhuInput = Double.parseDouble(inputText);
            String dariSkala = (String) dariComboBox.getSelectedItem();
            String keSkala = (String) keComboBox.getSelectedItem();

            double suhuCelcius = konversiKeCelcius(suhuInput, dariSkala);
            double hasil = konversiDariCelcius(suhuCelcius, keSkala);

            hasilField.setText(String.format("%.2f Â°%s", hasil, keSkala.substring(0, 1)));
        } catch (NumberFormatException ex) {
            hasilField.setText("Input tidak valid");
        }
    }

    private double konversiKeCelcius(double suhu, String dariSkala) {
        return switch (dariSkala) {
            case "Fahrenheit" -> (suhu - 32) * 5 / 9;
            case "Reamur" -> suhu * 5 / 4;
            case "Kelvin" -> suhu - 273.15;
            default -> suhu;
        };
    }

    private double konversiDariCelcius(double suhuCelcius, String keSkala) {
        return switch (keSkala) {
            case "Fahrenheit" -> (suhuCelcius * 9 / 5) + 32;
            case "Reamur" -> suhuCelcius * 4 / 5;
            case "Kelvin" -> suhuCelcius + 273.15;
            default -> suhuCelcius;
        };
    }

