    private void setupEventListeners() {
        // Membatasi input hanya angka
        numberField.addKeyListener(new java.awt.event.KeyAdapter() {
            @Override
            public void keyTyped(java.awt.event.KeyEvent evt) {
                char c = evt.getKeyChar();
                if (!(Character.isDigit(c) || c == '-' || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE)) {
                    evt.consume();
                    getToolkit().beep();
                }
                if (c == '-' && numberField.getText().length() > 0) {
                    evt.consume();
                    getToolkit().beep();
                }
            }
        });
    }

    private String checkEvenOdd(long number) {
        if (number % 2 == 0) {
            resultLabel.setForeground(Color.BLUE);
            return "Bilangan GENAP";
        } else {
            resultLabel.setForeground(Color.RED);
            return "Bilangan GANJIL";
        }
    }

    private String checkPrime(long number) {
        if (number < 2) return "Bukan Prima";
        if (number == 2) return "Prima";
        if (number % 2 == 0) return "Bukan Prima";

        for (long i = 3; i * i <= number; i += 2) {
            if (number % i == 0) return "Bukan Prima";
        }
        return "Prima";
    }
