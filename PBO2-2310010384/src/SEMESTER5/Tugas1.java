    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed
        // TODO add your handling code here:
        String input = numberField.getText().trim();

    if (input.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Masukkan angka terlebih dahulu!",
                "Input Kosong",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        long number = Long.parseLong(input);
        String result = checkEvenOdd(number);

        if (primeCheckBox.isSelected()) {
            result += " | " + checkPrime(number);
        }

        resultLabel.setText(result);

        JOptionPane.showMessageDialog(this,
                "Angka " + number + " adalah:\n" + result,
                "Hasil Pengecekan",
                JOptionPane.INFORMATION_MESSAGE);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Input harus berupa angka bulat!",
                "Input Tidak Valid",
                JOptionPane.ERROR_MESSAGE);
        clearButtonActionPerformed(evt);
    }
                                               
    }//GEN-LAST:event_checkButtonActionPerformed

